        public SettingsWindow()
        {
            InitializeComponent();
            tbx_settingConnectionBroker.Text = GUIStateManager.GUIStateInstance.connectionBroker;
        }

        private void btnCancel_Click(object sender, RoutedEventArgs e)
        {
            this.Close();
        }

        private void btnSave_Click(object sender, RoutedEventArgs e)
        {
            GUIStateManager.GUIStateInstance.connectionBroker = tbx_settingConnectionBroker.Text;
